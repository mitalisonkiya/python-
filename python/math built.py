import math

# Square root
print("Square root of 16:", math.sqrt(16))

# Power function (x^y)
print("2 raised to the power of 3:", math.pow(2, 3))

# Trigonometric functions (sin, cos, tan)
print("sin(30 degrees):", math.sin(math.radians(30)))
print("cos(45 degrees):", math.cos(math.radians(45)))
print("tan(60 degrees):", math.tan(math.radians(60)))

# Ceiling and floor functions
print("Ceiling of 3.7:", math.ceil(3.7))
print("Floor of 3.7:", math.floor(3.7))

# Factorial
print("Factorial of 5:", math.factorial(5))

# Constants
print("Value of pi:", math.pi)
print("Value of e (Euler's number):", math.e)
